    private void leerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_leerActionPerformed
        fileSelector.setDialogTitle("Escoja el fichero con las instrucciones para el robot");
        //fileSelector.setDialogType(JFileChooser.OPEN_DIALOG);

        if (fileSelector.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            try {
                BufferedReader input = new BufferedReader(new FileReader(fileSelector.getSelectedFile()));
                try {
                    JRSrcEditor.setText("");
                    String line = null;
                    while ((line = input.readLine()) != null) {
                        JRSrcEditor.append(line + "\n");
                    }
                } finally {
                    input.close();
                }
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_leerActionPerformed

    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
        fileSelector.setDialogTitle("Diga el nombre del fichero que utlizar√° para salvar las instrucciones");
        //fileSelector.setDialogType(JFileChooser.OPEN_DIALOG);

        if (fileSelector.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
            Writer output = null;

            try {
                output = new BufferedWriter(new FileWriter(fileSelector.getSelectedFile()));
                output.write(JRSrcEditor.getText());
             
            } catch (IOException ex) {
                ex.printStackTrace();
            }
            finally {
                try {
                    output.close();
                } catch (IOException ex) {
                    Logger.getLogger(Editor.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }

    }//GEN-LAST:event_salvarActionPerformed

